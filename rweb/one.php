<center><h3><font color=#008000>
ALFALFA: HI Cosmology in the Local Universe</font></h3></center>

<font color=#005000>
The Arecibo Legacy Fast ALFA (ALFALFA) survey is an 
on-going second generation blind extragalactic HI survey 
exploiting Arecibo's superior sensitivity, angular 
resolution and digital technology to conduct a census 
of the local HI universe over a cosmologically significant 
volume. When complete in 2012, ALFALFA will have detected
more than 30,000 extragalactic HI line sources out 
to z~0.06, and its catalog will be especially useful 
in synergy with wide area surveys conducted at other 
wavelengths. ALFALFA is detecting HI masses as low as 
one million solar masses and as high as one hundred billion solar masses 
with positional accuracies typically better than 20 arcsec, 
allowing immediate identification of the most probable 
optical counterpart to each HI detection. 
ALFALFA results already suggest, in agreement with 
the suggestions of previous, more limited studies, that there 
does not appear to be a cosmologically significant population 
of optically dark but HI rich galaxies; fewer than 2% of the 
extragalactic HI line sources detected by ALFALFA cannot be identified 
with a feasible optical counterpart. In the region 
of the Virgo cluster of galaxies, a number of optically 
dark HI sources have been found. These all lie in the 
outskirts of the cluster and could be tidal or "harassment" 
debris, the result of high speed gravitational encounters.
Additionally, ALFALFA has identified a intriguing population of massive 
galaxies with unusually high fractions of their baryons in the form of 
cool HI gas: how have these galaxies managed to maintain such massive
HI disks without converting their gas into stars? At the low mass end,
ALFALFA has already detected several hundred galaxies with
HI masses less than 100 million solar masses, and we have identified
a class of ultracompact high velocity clouds which match the
predictions for gas-bearing Local Group mini-halos.
The 2011 ALFALFA 
sample approaches homogeneity but is not yet a perfectly representative 
sample of the nearby volume; the full ALFALFA survey, when completed, 
will be. ALFALFA promises 
a wealthy dataset for the exploration of many issues 
in near-field cosmology and galaxy evolution studies, 
setting the stage for their extension to higher 
redshifts with the Square Kilometer Array.</p>
