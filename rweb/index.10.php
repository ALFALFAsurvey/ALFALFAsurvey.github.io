<P>
<FONT SIZE="+1"><B>Overview</B></FONT> 

<P><img src="aodawn.jpg" width="150" height="200" border="0" alt="Arecibo Telescope" align="left">
Arecibo is the world's most sensitive radio telescope at L-band.
In addition to that all-important sensitivity advantage, Arecibo equipped with 
<a href="http://egg.astro.cornell.edu/alfalfa/ugrad/alfa.htm">
ALFA</a> offers
important and significant improvements in angular and spectral resolution 
over the available major wide area extragalactic HI line surveys such as HIPASS and HIJASS.
To break ground into new science areas, extragalactic HI surveys with ALFA
must exploit those capabilities to explore larger volumes with greater sensitivity than have
the previous surveys. The lowest mass objects will only be detected nearby; 
wide areal coverage is the most efficient 
means of increasing the volume sampled locally. An extragalactic survey covering the high 
galactic latitude sky visible from Arecibo will produce an extensive database of HI spectra 
that will be of use to a broad community of investigators, including many interested in 
the correlative mining of multiwavelength datasets; we thus dub this program the
<b><font color=#008000><i>Arecibo Legacy Fast ALFA</I></font></b>
 survey: <b><font color=#008000>ALFALFA</font></b>. A comparison of major blind HI surveys 
and ALFALFA is presented in <a href="./tableb1.php">
Table B.1</a>. A <a href="http://egg.astro.cornell.edu/alfalfa/ugrad/observingmode.htm">
2-pass drift survey</a> will deliver
1.6 mJy/channel sensitivity (at 18 km/s), 8&times; better than HIPASS and with 4&times;
better angular resolution (FWHM). In addition to its broad applications, such a wide area 
HI survey will serve as a strategic approach to a number of focussed E-ALFA science 
objectives. In coordination with this survey, deeper studies of selected regions, some 
of which await the second generation E-ALFA spectrometer, will address other critical 
E-ALFA science goals that are not discussed herein. 

<P>
When completed in 4-5 years, ALFALFA will 
survey 7000 deg<sup>2</sup> and will detect 
more than 25000 extragalactic HI sources. It is specifically designed to probe 
the faint end of the HI mass function (HIMF) in the very local universe and will 
provide a complete census of HI in the surveyed sky area, making it especially useful 
in synergy with other wide area surveys such as SDSS, 2MASS, GALEX, etc. In conjunction 
with optical studies of comparable volumes, ALFALFA will explore the 
"missing satellite problem", the apparent contradiction between the number of 
low mass halos observed in the Local and surrounding groups with that predicted from 
numerical simulations. ALFALFA will also provide the basis for studies of the dynamics
of galaxies within the Local and nearby superclusters, will allow measurement of the 
HI diameter function, and will enable a first wide-area blind search for local HI tidal
features, HI absorbers at <i>z</i> < 0.06 and OH megamasers in the redshift range 
0.16 < <i>z</i> < 0.25. It will also detect, in abundance, some of the very high
HI mass galaxies which will be the lampposts for future HI surveys planned
for the Square Kilometer Array.</p>

<P>
On Feb 4th, 2005, we began the ALFALFA program as a 2-pass, 40 sec/beam E-ALFA high 
galactic latitude drift-scan survey. The ALFALFA observing strategy exploits a simple 
<a href="http://egg.astro.cornell.edu/alfalfa/ugrad/observingmode.htm">fixed-azimuth drift scan
</a> -- <I>minimum intrusion</I> -- technique, 
an approach used by others in the past, refined through extensive single pixel test 
observations in 2003-4 (AO programs A1705, A1763 and A1849) and finally implemented 
for ALFA in precursor observations in Aug-Sep 2004 (A1946). A 
<a href="http://egg.astro.cornell.edu/alfalfa/ugrad/twopass.htm">
2-pass strategy</a> will 
greatly aid in the rejection of spurious signals and rfi, thus minimizing the need 
for follow-up confirmation observations, will even out the scallopping in the maps that 
arises from unequal pixel gain, and offers the opportunity to use the same dataset
for the statistical characterization of continuum transients. A 
<a href="coverage.php">wide-area high latitude 
survey</a> is of interest to the G-ALFA and P-ALFA consortia once commensal observing 
becomes possible; expressions of interest in commensality have been obtained from
G-ALFA and P-ALFA team leaders. The current WAPP capability is well matched to ALFALFA's 
goals. Restriction to the 100 MHz from 1335 to 1435 MHz minimizes the vulnerability of the 
observing program to RFI, particularly severe below 1335 MHz.

<P>
During the course of our ALFA precursor observations in 2005, we demonstrated through their
results (see the <a href="http://www.astro.cornell.edu/~haynes/pre204/drift.htm">A1946
drift precursor</a> page) that the fixed azimuth
drift scan technique produces well calibrated, bandpass flattened datasets. We have already 
invested significant effort in developing software in the IDL environment for data processing 
and for signal detection. We have established a collaboration with the US National
Virtual Observatory that allows public access to the resultant 
<a href="http://arecibo.tc.cornell.edu/hiarchive/alfalfa/"> data products</a>
on the shortest possible timescale. Other efforts by members of the ALFALFA team
will focus on complementary and follow-up multiwavelength observations, on development of
velocity flow models, clustering algorithms and numerical simulations, and on the
development of educational and public outreach materials. Students at both the graduate 
and undergraduate level are already involved in conducting the observations and in analyzing 
survey data and their scientific implications.</p>

ALFALFA is on-going but already a number of interesting results are appearing.
ALFALFA is detecting HI masses as low as 10<sup>6</sup> solar
masses and as high as 10<sup>10.8</sup> solar masses with positional accuracies typically
better than 20 arcsec, allowing immediate identification of the most probable optical
counterparts to each HI detection. In the region of the Virgo cluster of galaxies,
a number of optically dark HI sources have been found. These all lie in the outskirts
of the cluster and could be tidal or "harassment" debris, the result of high speed
gravitational encounters 
(<a href="http://adsabs.harvard.edu/abs/2007ApJ...665L..15K">Kent <i>et al</i> 2007</a>).
Most notably, the object VirgoHI21, suggested by the Cardiff group to be a "dark galaxy",
is clearly seen in the <a href="virgohi21.php">far superior ALFALFA map</a> 
as merely one condensation in a 250 kpc
long tail extending from the bright, optically asymmetric spiral NGC 4254
(<a href="http://adsabs.harvard.edu/abs/2007ApJ...665L..19H">Haynes <i>et al</i> 2007</a>),
which had previously been suggested to be falling into the Virgo cluster for the
first time. Based on the ALFALFA catalog available in mid-2007 of
some 4500 sources in 15% of the final survey area, we find that only 3% of all extragalactic
HI sources and few than 1% of detections with HI masses greater than 10<sup>9.5</sup>
solar masses cannot be identified with a stellar component. First ALFALFA results
already suggest, in agreement with previous studies, that there does not appear to 
be a cosmologically significant population of
optically dark but HI rich galaxies.  ALFALFA promises
a wealthy dataset for the exploration of many issues in near-field cosmology
and galaxy evolution studies, setting the stage for their extension
to higher redshifts with the Square Kilometer Array (SKA).</p>

