<font color=#008000><h2> The Observing Team</h2></font>

The Observing Team effort is coordinated by Martha Haynes.</p>

We observe a lot --- in 2005, the ALFALFA observing program A2010 was allocated 155
observing blocks on the Arecibo telescope. Because of the high demand for telescope time
and the need to accomodate many different observing programs,
it is more convenient for NAIC's schedulers to allocate telescope time for the
ALFALFA survey in more but shorter blocks than we had originally requested. 
As a result, we cover the same area of sky as
proposed, but in a somewhat less convenient (for us) way. Survey <a href=
"http://egg.astro.cornell.edu/alfalfa/scheds/index.php">progress</a> is carefully
planned to accomodate both the survey strategy and the practical aspects of
telescope time allocations. Thanks to the team effort, we've had great success
in our observing -- and a lot of fun, too!</p>

To enhance interaction and exchange between
the Observatory staff and the observing team, the NAIC director urges us to be
on site whenever possible so we do try to make sure that we have a regular presence
in Arecibo.
At other times, when classes and other obligations keep us home or scheduling
is spotty, we observe remotely from selected locations.  A2010 requires continuous 
monitoring of an attentive observer during data acquisition and 
daily attention to data quality, data transfer and storage,
and general survey management.

Our observing team is committed to making most efficient and effective use of every
minute of telescope time, and we have set up a detailed protocol for all tasks associated
with planning and carrying out the observations as well as archiving and processing the
data. To engage the broadest possible team, ALFALFA "experts" provide
on-site training in all aspects of observing and data reduction to others who then
assume the observing-specific responsibilities for some period of time. Here is some
evidence of the teamwork that results.</p>

