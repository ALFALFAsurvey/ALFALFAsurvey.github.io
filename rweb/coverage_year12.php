<P>
ALFALFA will cover 7074 square degrees of the high galactic latitude sky
accessible to the Arecibo telescope, i.e. the regions between 0&deg;
and +36&deg; in Declination (subdivided into 9 bands of tiles, each
4&deg; wide), 07<sup>h</sup>30<sup>m</sup> to 16<sup>h</sup>30<sup>m</sup> and
22<sup>h</sup> to 03<sup>h</sup> in Right Ascension. The full region
of the survey will be mapped in drift mode, following a 2--pass strategy
(i.e. each Declination band will be scanned at two different epochs,
separated by a few months) with the ALFA system, requiring a total of
4130 hours of telescope time. The completion
of the full survey is projected to require 5 years.</p>

