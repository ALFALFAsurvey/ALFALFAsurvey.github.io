<a name="sciencegoals"></a><FONT SIZE="+1"><B><font color=#008000>Science Goals</font></B></FONT>

<P>
The upgrade of the surface of the Arecibo antenna in the mid-1970's 
initiated a new era of extragalactic 21 cm HI line studies which
exploited the big dish's collecting area and superior ancillary
instrumentation (low noise amplifiers; broadband, flexible multi-bit
spectrometers). The local extragalactic sky 
<a href="coverage.php">visible to Arecibo</a> is rich, 
containing the central longitudes of the Supergalactic Plane in and around the
Virgo cluster, the main ridge of the Pisces-Perseus Supercluster,
and the extensive filaments connecting A1367, Coma and Hercules.
With ALFA, the Arecibo legacy of extragalactic HI studies will
continue, probing regimes untouched by other surveys and addressing
fundamental cosmological questions (the number density, distribution
and nature of low mass halos) and issues of galaxy formation and
evolution (sizes of HI disks, history of tidal interactions and mergers,
low z absorber cross section, origin of dwarf galaxies, nature
of high velocity clouds). Here we briefly outline the main science objectives 
of the E-ALFA wide area high galactic latitude program.

<P>
<a name="legacysurvey"></a><B><font color=#cc0514>2.1 A Legacy Survey: HI 
in the Nearby Universe</font></B> &nbsp &nbsp
The survey design and its strategy as proposed here have evolved from 
numerical simulations (c.f. Giovanelli 2003; Masters <I>et al.</I>&nbsp;2004), in which
we use a cosmic density map provided by the PSCz density reconstruction (Branchini
<I>et al.</I>&nbsp;1999) gridded with 0.9375h<sup>-1</sup> Mpc spacing in the inner 
60h<sup>-1</sup> Mpc, and at twice that value between 60 and 120h<sup>-1</sup> Mpc;
the map is smoothed with a Gaussian filter of 3.2h<sup>-1</sup> Mpc. The
density map is complemented by a peculiar velocity map, which allows
us to infer more accurate estimates of the distances than those
derived purely from redshifts. Two different HIMFs (those derived by
Zwaan <I>et al.</I>&nbsp;1997 and Rosenberg &amp; Schneider 2002; the Zwaan
<I>et al.</I>&nbsp;2003 HIMF has values intermediate between the former two) 
are used to populate the map with HI "clouds", which we then proceed to 
"detect". HI sizes and velocity widths are assigned 
using empirical scaling relations obtained from our own HI survey 
data and Broeils &amp; Rhee (1997), with realistic scatter and spectral baseline
instability. Disk inclinations and pointing offsets are randomized. 
We have inspected a wide grid of survey parameters, including different 
scenarios of suppression of gas infall onto low mass halos due to 
reionization. Experiments made with the single pixel L-narrow receiver
in 2003 and preliminary inspection of our recent A1946 precursor 
observations confirm the efficacy of these simulations in practical
observing conditions (Giovanelli <i>et al</i>  
<a href="http://adsabs.harvard.edu/cgi-bin/nph-bib_query?bibcode=2005AJ....130.2598G&amp;db_key=AST&amp;data_type=HTML&amp;format=&amp;high=42a8b3a4c131572">
2005a;
<a href="http://adsabs.harvard.edu/cgi-bin/nph-bib_query?bibcode=2005AJ....130.2613G&amp;db_key=AST&amp;data_type=HTML&amp;format=&amp;high=42a8b3a4c131572">
2005b).

<P>
Our early simulations, based on past estimates of the HI Mass Function predicted that
ALFALFA would detect more than 16000 objects. 

sampling 
a wide range of hosts from local, very low HI mass dwarfs to gas-rich massive 
galaxies seen to z ~ 0.06. HI spectra provide redshifts, HI masses and rotational
widths for normal galaxies, trace the history of tidal events and provide quantitative
measures of the potential for future star formation via comparative HI contents.
As a blind HI survey, ALFALFA will not be biased
towards the high surface brightness galaxies typically found in optical galaxy
catalogs and moreover, in contrast to HIPASS and HIJASS, will have adequate angular
and spectral resolution to be used on its own, without the need for followup
observations to determine identifications, positions and, in many cases, characteristic
HI sizes. The <a href="coverage.php">wide areal coverage</a> of ALFALFA overlaps with several
other major surveys, most notably the Sloan Digital Sky Survey (SDSS), 2MASS and
the NVSS. The catalog products of ALFALFA will be invaluable for multiwavelength
data mining by a wide spectrum of astronomers, far beyond those currently engaged
in the ALFALFA survey itself. A key element of this program is to provide 
broad application, legacy data products that will maximize the science fallout.

<P>
<a name="himassfunction"></a><B><font color=#cc0514>2.2 The HI Mass Function and the
"Missing Satellite Problem"</font></B> &nbsp &nbsp
One of the principal discrepancies between cold dark matter (CDM) theory and 
current observations revolves around the large difference between the number
of dwarf dark matter halos seen around giant halos in numerical simulations
based on CDM and the observed dwarf satellite population in the Local Group
(Kauffmann <I>et al.</I>&nbsp;1993; Klypin <I>et al.</I>&nbsp;1999; 
Moore <I>et al.</I>&nbsp;1999b), referred
to as the "missing satellite problem".
The logarithmic slope of the faint end of the galaxy mass function 
predicted by CDM simulations is close to the value of &alpha; = -1.8
that arises analytically from the Press-Schechter formalism (Press &amp;
Schechter 1974; Bardeen <I>et al.</I>&nbsp;1986). Because the mass function itself 
is difficult to determine directly, current efforts focus on estimation 
of the faint end of the optical luminosity function (LF) and, of direct 
relevance to this proposal, of the HIMF. By determining
both, limits can be set on the number of low mass halos containing measurable
stellar or gaseous components. The shape of the low mass end of the HIMF
and its corollary, the cosmological mass density of HI, are important
parameters in the modelling of the formation and evolution of galaxies. 

<P>
The HIMF is the probability distribution over HI mass of detectable HI line
signals in a survey sensitive to the global neutral hydrogen within a system.
The most recent estimates of the HIMF have 
been presented by Zwaan <I>et al.</I>&nbsp;(1997; Z97), Rosenberg &amp; Schneider (2002; 
RS02), Zwaan <I>et al.</I>&nbsp;(2003; Z03) and Springob <I>et al.</I>&nbsp;(2004). The latter is
derived from a compilation of some 9000 optically selected galaxies, further
restricted by HI line flux and optical diameter to a complete subsample
containing 2200 galaxies. The other determinations are based on blind
HI surveys and thus have no bias against the low luminosity and low
optical surface brightness galaxies which may be underrepresented in
optical galaxy catalogs. The Z03 HIMF is based on the HIPASS survey (Koribalski 
<I>et al.</I>&nbsp;2004; Meyer <I>et al.</I>&nbsp;2004), while the RS02 and Z97 HIMFs are both based 
on drift scan surveys conducted at Arecibo during the period of its recent
upgrade. The faint end slope of those determinations of the HIMF vary 
between -1.20 and -1.53, yielding extrapolations below M<sub>HI</sub> = 10<sup>7</sup> M<sub>sun</sub>
that disagree by an order of magnitude, the RS02 HIMF having the
steeper slope.  All three HI blind surveys sample a lower mass limit just below 
M<sub>HI</sub> = 10<sup>8</sup> M<sub>sun</sub>, for H<sub>&deg;</sub> = 
70 kms<sup>-1</sup>Mpc<sup>-1</sup> (a value that will be assumed throughout, while
for Virgo we adopt a distance D = 16 Mpc).
No galaxies were detected by RS02 or Z97 with 
M<sub>HI</sub> < 10<sup>7</sup> M<sub>sun</sub>, while 3 are claimed by Z03, and
only a small number of detections have M<sub>HI</sub> < 10<sup>8</sup> M<sub>sun</sub>.</p>

<P>
As pointed out by Kratsov <I>et al.</I>&nbsp;(2004), models of the formation of large scale 
structure must explain not only the number of satellites found in the Local Group, 
but also their clustering characteristics: whereas the dSphs are found concentrated with 
~300 kpc of their host giant galaxies, the irregulars are spread throughout, 
both near and far from the giants (Grebel 2004). The origin of this segregation as
well as the fundamental differences among the dwarf populations are thus important
issues for galaxy formation theories. The possible variation in the HIMF with 
local galaxy density or velocity dispersion can provide a statistical measure
of the impact of environment mechanisms on the gas as galaxies evolve. 

<P>
Surveys using ALFA will explore two fundamental aspects of the
HIMF: its low mass slope, which has a direct bearing on the
"missing satellite problem", and its behavior with varying galaxy environment.
To date, studies of the possible
environmental dependence of the HIMF have been limited to comparisons
of the HIMF derived for galaxies in the Virgo cluster with those in the 
field (Hoffman <I>et al.</I>&nbsp;1992; Briggs &amp; Rao 1993; RS02; Davies <I>et al.</I>&nbsp;2004; 
Gavazzi <I>et al.</I>&nbsp;2004) but suffer from poor statistics and incompleteness. 
The results marginally suggest that the HIMF in Virgo is missing the low 
HI mass dwarfs found in the field or is at least flatter at the faint end 
than the field HIMF. 

<P>
The science program of the E-ALFA consortium as illustrated in the E-ALFA white paper
has, as one of its main goals, the robust determination of the HIMF over a range of 
independent volumes characterized by varying cosmic density. No single survey is
likely to explore all the relevant parameter space. Achieving adequate detection 
statistics for objects in the 10<sup>6</sup> - 10<sup>8</sup>M<sub>sun</sub> range
requires a balance of survey areal coverage and
survey depth in order to sample adequate volume. Studying a wide range
of environments likewise necessitates tradeoffs of depth and area. In
combination with the deeper surveys proposed under the 
<a href="http://www.naic.edu/~ages">AGES</a> (Arecibo
Galaxy Environments Survey) program, ALFALFA will allow exploration of a
wide range of possible HIMF scenarios. It will focus on studies of the
lowest mass objects in the very nearby universe (M<sub>HI</sub> < 
10<sup>7</sup> M<sub>sun</sub>, D < 15 Mpc) within the Local Supercluster but will also explore 
variations of the global HIMF, suggested by Springob <I>et al.</I>&nbsp;(2004) by 
sampling, at higher masses, across the range of local densities that 
characterize the rich clusters like A262, A1367 and Coma, their supercluster 
filaments and the voids between them. 

<P>
We emphasize that since the lowest HI masses will be found only
very locally, ALFALFA must cover a very large solid angle in order to survey
adequate volume at D < 15 Mpc. ALFALFA as proposed
will detect between 60 and 300 objects with 
M<sub>HI</sub> < 10<sup>7.5</sup> M<sub>sun</sub> depending
on whether the HIMF follows Z97 or RS02. Both the legacy aspect and the local
volume requirement thus dictate the need to survey 7000 deg<suP>2</sup>. 

<P>
<a name="galaxyevolution"></a><B><font color=#cc0f14> 2.3 Galaxy Evolution and 
Dynamics within Local Large Scale Structures</font></B> &nbsp &nbsp 
The large scale distribution of galaxies in the local universe is 
concentrated in a structure (Lahav <I>et al.</I>&nbsp;2000) first recognized by 
de Vaucouleurs and today designated as the Supergalactic Plane. At its 
center, the Virgo cluster is the nearest rich cluster to us. Overall, the 
galaxy distribution in that direction has been shown to trace a filamentary 
structure (West &amp; Blakeslee 2000; Gavazzi <I>et al.</I>&nbsp;1999; Solanes <I>et al.</I>&nbsp;2002) 
elongated along the line of sight. Galaxies in the cluster core are known 
to be HI-deficient due to interaction with the hot intracluster gas, while 
galaxies in the cluster periphery, foreground and background are not. Including 
its several principal concentrations, the cluster extends about 
14&deg; over the sky (Binggeli, Popescu &amp; Tammann 1993). The solid 
angle subtended by this region samples the highest densities in the local
Universe, and thus constitutes the obvious choice for the study of the HIMF in a 
high density environment. A region of comparable volume but low
density, surveyed to comparable sensitivity, is required to provide a 
reference. The regions with lowest cosmic density at comparable distance
are, unsurpringly, in the anti-Virgo direction. Optimization of the 
<a href="coverage.php">Arecibo sky coverage</a>
and zenith angle dependence of sensitivity suggests an anti-Virgo 
region centered near 1.5<sup>h</sup> in R.A. and +24&deg; in Dec. This region includes 
a large section of the largest, nearby cosmic "void"; averaged over a 
solid angle of ~0.25 sterad, the anti-Virgo region between 0 and 
3000 km/s is underdense by a factor of ~6 with respect to the Virgo region 
in the same distance range. The comparative study of the HI and other properties
of the galaxies in these two regions will yield clues on the procesess of
environmental influence on galaxy evolution. HI contents will be compared, the dwarf
population will traced over wide ranges of cosmic density, and a first
truly blind survey for HI tidal remnants will be made.

<P>
A wide area Virgo survey will provide a database of unprecedented breadth for the
investigation of the origin of gas deficiency in that cluster which will complement nicely
the targeted, higher spatial resolution HI line synthesis study of Virgo galaxies 
currently being undertaken with the Very Large Array (PI: J. Kenney).  It
will also improve the dynamical understanding of the cluster
and its surrounding groups, as well as of the processes associated
with the evolution of large-scale structure, by providing a rich 
redshift data base of low optical luminosity gas-rich dwarfs not only within
the cluster core but also in its broad surroundings. 

<P>
Virgo is the only environment which is both near enough that distance estimates based on
secondary methods can distinguish between infall and expansion regimes in the
region around the cluster (the so-called "triple-valued region") and 
also massive enough to possess an extensive, well-populated infall region.
In the case of Virgo, the infall domain extends 28&deg; from the center of the 
cluster, so that a survey that can identify objects at turnaround must cover
a very wide area. While the SDSS may provide the required
photometric parameters for applications of the Tully-Fisher distance method,
its 3 arcsec fibers cannot provide adequate rotational width measures. Thus
ALFALFA, in combination with the SDSS database, will provide the basis for
a unique study of the galaxy dynamics both in and around the Virgo cluster.

<P>
Other groups within the Local Supercluster will also be targets including:
the Canes Venatici I Group at about 5 Mpc, the Leo&nbsp;I group at about 10 Mpc,
the "groups of dwarfs" (Tully <I>et al.</I>&nbsp;2002) around UGC&nbsp;3974 (D = 5.4 Mpc) and
NGC&nbsp;784 (D = 4.4 Mpc), and the Canes Venatici II and Coma I groups at 
at 10-20 Mpc.  There are  ~20 additional groups at 
velocities less than 1000 km/s which we should be able to study in great detail.
Models of the structure of the Local Supercluster are being developed by KLM and
by IDK and collaborators and will both contribute to and benefit from the
ALFALFA survey.

<P>
Of particular note, the Leo&nbsp;I (M&nbsp;96) Group offers an attractive
opportunity for exploring both the optical luminosity function and
the HIMF in an intermediate density environment. Unlike
the Local Group, Leo&nbsp;I is dominated by early type galaxies, yet
it is still characterized by a low velocity dispersion. For 19 galaxies
with measured redshifts, the dispersion in radial velocity is 130 km/s.
Two of the brightest galaxies in Leo I - NGC&nbsp;3379, and NGC&nbsp;3384 - are surrounded
by a 200&nbsp;kpc ring of HI gas (Schneider <I>et al.</I>&nbsp;1983). Two possible scenarios
for the origin of this cloud have been proposed. Rood &amp; Williams (1985) suggested
that the ring resulted from a collision between NGC&nbsp;3384 and NGC&nbsp;3368 some
500 Myr ago. After the discovery of several additional gas features, Schneider (1985)
noted that the clouds appear to be stable against tidal disruption and proposed
that they instead represent a remnant of the primordial gas cloud from which
all of the group members formed. Recently uncovered kinematic signatures suggest
that all of the brighter galaxies have been involved in past interactions
(Sil'chenko <I>et al.</I>&nbsp;2003). Thus the Leo&nbsp;I region presents an interesting environment in which
to study differences among the low luminosity dwarf populations: a region
of low velocity dispersion but containing a local density enhancement that
supports the presence of bright E/S0 galaxies. 
The methodology developed by IDK &amp; VEK to identify nearby dwarfs has already
uncovered considerable numbers of faint gas-rich members of other nearby groups
(e.g., Karachentseva &amp; Karachentsev 1998; Karachentseva <I>et al.</I>&nbsp;1999, 2001;
Makarov, Karachentsev &amp; Burenkov 2003). To probe the dI population found
by Karachentsev &amp; Karachentseva (2004), a very wide field (> 120 square deg), as provided
by ALFALFA, must be studied. 

<P>
<a name="extent"></a><B><font color=#cc0f14>2.4 The Extent and Origin of HI Disks</font></B>
&nbsp &nbsp
Extended gas disks around galaxies represent a reservoir for future star 
formation activity. The study of the distribution of HI relative to that
of the optical (stellar) disk allows the investigation of the
relationship of gas to star formation and the discrimination of models
of the origin of the observed truncation of stellar disks at 3 - 5 optical disk scale
lengths based on gas density threshholds (Fall &amp; Efstathiou 1989) versus
those related to the maximum protogalaxy specific angular momentum (van der Kruit 1979).
In contrast to other major wide area surveys such as HIPASS and HIJASS, some 500
gas-rich galaxies will be resolved by ALFA's 3.5 arcmin beam, allowing 
a quantitative measure of their characteristic HI sizes (Hewitt <I>et al.</I>&nbsp;1984) and 
the derivation of the HI diameter function. In combination with optical photometry,
ALFALFA will determine the fraction of galaxies with extended gas disks and enable studies 
of their host galaxies, their environment, morphology and the role of gas in their
evolution. Of particular note, we hope to discover more extremely extended gas disks,
such as those found in DDO&nbsp;154 (Krumm &amp; Burstein 1984), NGC&nbsp;4449 (Bajaja <I>et al.</I>&nbsp;1994),
NGC&nbsp;2915 (Meurer <I>et al.</I>&nbsp;1996) and UGC&nbsp;5288 (van Zee 2004) and extensive tidal
features such as those seen in the Leo Triplet (Haynes, Giovanelli &amp; Roberts 1979).

<P>
Additionally, ALFALFA will resolve extended HI in the vicinity of the 100
large (D<sub>UGC</sub> > 5 arcmin) nearby galaxies that may have been missed by
interferometric observations, allowing for a census of the neutral ISM on
all spatial scales. In particular, the Arecibo telescope provides an ideal
probe of the short spacings missed by the VLA in its more compact
configurations.  For the ALFALFA parameters t<sub>s</sub> = 28 s/beam and channel
bandwidth of 5 km/s, the antenna temperature detectable at the 5 &sigma; limit
is T<sub>A</sub> = 0.13 K; for a source with a spectral width of 25 km/s which fills
the beam, this limit corresponds to a minimum detectable column
density of N<sub>HI</sub> ~6 x 10<sup>18</sup> cm<sup>-2</sup>.  

<P>
The column density regime probed by ALFALFA will characterize the
broad-scale emission at the edges of galaxy disks, which are hypothesized
to truncate at roughly the same value of N<sub>HI</sub> (e.g. Corbelli &amp; Salpeter
1993, Maloney 1993). The existence of a smooth HI component similar to
that in DDO 154 (Hoffman <I>et al.</I>&nbsp;2001) would also extend rotation curves
further into the dark matter halo, allowing for more robust determination
of the halo shape and concentration to contrast with cold dark matter 
paradigm predictions on galaxy scales (e.g. Dutton <I>et al.</I>&nbsp;2004; Barnes 
<I>et al.</I>&nbsp;2004).

<P>
ALFALFA will provide a census of the abundance and distribution of 
HI disks, providing the low redshift link to the damped Lyman &alpha; (DLA)
absorption seen in quasar spectra. At higher redshift, the neutral gas mass
traced by DLA absorption makes a greater contribution to the luminous baryonic 
mass than it does at the current epoch. ALFALFA will provide important clues to such
gas disk evolution. While the HIMFs derived from all surveys to 
date suggest that large galaxies contribute the majority of the local HI mass 
density, it also seems that massive galaxies do not dominate the cross section 
for DLA absorption (Rosenberg &amp; Schneider 2001; Rao &amp; Turnshek 1998). 
Recent observations with the GMRT of resolved low <i>z</i> DLAs similarly has 
found that the absorption is associated with HI masses less
than those characteristic of L* galaxies (Chengalur &amp; Kanekar 2002). 
To understand the DLA cross section at low redshifts requires study of 
the population of low mass but HI rich galaxies that are missing from
optical catalogs. Followup optical studies of ADBS counterparts by JLR and JJS demonstrate
that the earlier survey detected galaxies with absolute magnitudes of -16 at distances 
of 70 Mpc. ALFALFA should detect such objects in very large numbers, allowing
not only robust estimates of their contribution to the local HI cross section, but
also a measure of their clustering correlation amplitude and scale.

<P>
<a name="highvelocityclouds"></a><B><font color=#cc0f14>
2.5 The Nature of High Velocity Clouds</font></B> &nbsp &nbsp
In addition to providing important clues on the extents and kinematics of HI
gas around other galaxies, ALFALFA will allow a wide area study of gas in and
around the Milky Way as a complement to, and in conjunction with, the G-ALFA surveys.
In particular, ALFALFA will explore the nature of the local high velocity
clouds (HVCs) of neutral hydrogen which may represent gas accretion onto 
our Galaxy (e.g., Tripp <I>et al.</I>&nbsp;2003) but which also have been claimed to be more
distant, the "missing satellites" in the Local Group (Blitz <I>et al.</I>&nbsp;1999; Braun &amp;
Burton 1999). Previous surveys of HVCs have
been of substantially lower resolution (15.5 arcmin at best) and/or
were unable to trace the connection between HVCs and Galactic HI
emission (Putman <I>et al.</I>&nbsp;2002; Wakker &amp; van Woerden 1991).
ALFALFA will trace important high-velocity structures, such as the northern 
portions of the Magellanic Stream and Complex C at 4&times; better
resolution than HIPASS.  It will also be 8&times; more
sensitive to unresolved small clouds, or ultra-compact HVCs
(if any exist with central neutral column density above 10<sup>20</sup>
cm<sup>-2</sup>). This will allow us to determine if HVCs
are interacting with a diffuse halo medium (e.g., Br&#252;ns <I>et al.</I>&nbsp;2000;
Quilis &amp; Moore 2001) and or if they are <I>bona fide</I> dark
matter-dominated Galactic satellites (e.g., Moore <I>et al.</I>&nbsp;1999a).

<P>
The recent discovery of an extended, faint population of HI clouds within 50 kpc
of M31 by Thilker <I>et al.</I>&nbsp;(2004) suggests a similar search for clouds around
M33. At the Andromeda distance, the Thilker <I>et al.</I>&nbsp;clouds have
masses between 10<sup>5</sup> - 10<sup>7</sup> M<sub>sun</sub>. While Arecibo can
not reach as far north as M31, ALFALFA will cover part of the region containing the clouds
discovered by Thilker <I>et al.</I>&nbsp;and their possible extension toward the region around M33.
Wright's cloud (Wright 1979; Braun &amp; Thilker 2004)
was detected easily in our A1946 observations in Aug/Sep 2004.

<P>
<a name="absorbers"></a><B><font color=#cc0f14>2.6 A Blind Survey for 
21 cm Absorbers at <i>z</i> < 0.06</font></B>  &nbsp &nbsp 
The background continuum source counts for this work at 1.4 GHz yield
2100 sources brighter than 0.4 Jy and 11400 sources brighter than 0.1 Jy,
within the survey area proposed.
A recent study by Vermeulen <I>et al.</I>&nbsp;(2004) searching for HI in absorption
in compact sources with the WSRT found absorption in 1/3 of the targeted sources and we
adopt their results as "typical" (although the redshift range is considerable higher).
Similarly, Darling <I>et al.</I>&nbsp;(2004) have detected HI in absorption in an "optically blind"
search against continuum sources using the GBT. The HI features found in those surveys
show a range of optical depths from &tau; = 0.16 to &tau; < 0.001 and
exhibit a variety of line profiles with widths as narrow as 10 km/s
but more typically! 150 km/s. For a source of 0.5 Jy, a peak absorption of 10 mJy 
(5&sigma;) corresponds to &tau; ~0.02. The peak column density is given by 
N<sub>HI</sub> 1.82 &times; 10<sup>18</sup> T<sub>spin</sub> &tau;<sub>peak</sub>
&Delta;V &nbsp cm<sup>-2</sup>. For T<sub>spin</sub> ~ 100K
and a velocity width of 100 km/s, &tau;<sub>peak</sub> ~ 0.02 corresponds to
N<sub>HI</sub> ~ 3.6 x 10<sup>20</sup> cm<sup>-2</sup> with the obvious condition 
that narrower widths would probe lower column densities. Using the values for 
&tau;<sub>peak</sub> and &Delta;V  given in Table 1
of Vermeulen <I>et al.</I>, we estimate that ALFALFA will be be able to detect all but three
of the lines found by those authors, assuming a frequency range match. 
ALFALFA will target low redshift absorbers not associated with the
radio sources themselves.

<P>
A major difficulty with absorption studies is spectral baseline determination. A method
commonly used averages sources of similar strength observed at comparable
telescope configuration (possible for the limited azimuth drift mode considered here).
We expect that standing waves will be 
broader than expected HI absorption lines, and that most rfi will be spectrally unresolved.
To identify absorbers, we will establish and follow a simple set of rules to assess whether 
or not a given spectral feature is RFI or real absorption.  This aspect of the project will 
require extra effort, but will yield cosmologically interesting statistics based
on such a "blind" HI absorption survey. Among others, JKD, EMM and CMS are interested in 
pursuing the absorption line study.

<P>
<a name="ohmegamasers"></a><B><font color=#cc0f14>2.7 A Blind Survey for OH Megamasers at 
 0.16 < <i>z</i> < 0.25</font></B> &nbsp &nbsp 
OH Megamasers (OHM) are powerful line sources observed in the L band,
arising from the nuclear molecular regions in merging galaxy systems.
Approximately 100 such sources are known to date, half of which were discovered
by JKD's Ph.D dissertation work (e.g. Darling &amp; Giovanelli 2002)
at Arecibo. Several of them are observed to have variable spectral features allowing 
superresolution and insight into the source structure and physics. Observations of 
OHMs hold the potential for tracing the merger history of the Universe since the 
sources are associated with merging galaxies. 














