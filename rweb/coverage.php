<FONT SIZE="+1"><font color=#cc0f14><B>ALFALFA Sky Coverage</B></font></FONT> 

<P>
ALFALFA was originially intended to cover 7074 square degrees of the high 
galactic latitude sky
accessible to the Arecibo telescope, i.e. the regions between 0&deg;
and +36&deg; in Declination (subdivided into 9 bands of tiles, each
4&deg; wide), 07<sup>h</sup>30<sup>m</sup> to 16<sup>h</sup>30<sup>m</sup> and
22<sup>h</sup> to 03<sup>h</sup> in Right Ascension. In the end, some parts of
the spring sky were observed only from 08<sup>h</sup>30<sup>m</sup> to 
15<sup>h</sup>30<sup>m</sup> so that the final coverage is only of
about 6300 square degrees.</p>

The survey was conducted in drift mode, following a 2--pass strategy
(i.e. each Declination band was scanned at two different epochs,
separated by a few months) with the ALFA system. The total allocation,
including overhead was 4,741.5 hours allocated in 808 separate
observing periods between 5 Feb 2005 and 26 Oct 2013.</p>

