<a name="comparison"></a><FONT SIZE="-1"><FONT SIZE="+1"><B><font color=#008000>
Comparison of E-ALFA Programs with Previous Blind HI Surveys:</font></B></FONT>
</FONT>
<P>
<FONT SIZE="-1">HIPASS and HIJASS cover the same area of sky that is visible at Arecibo,
HIPASS south of Dec.= +25&deg;, and HIJASS further to the north.
However, in addition to the large increase in sensitivity, ALFA surveys provide 2 direct 
benefits over the other two: improved angular and velocity resolution. 
The significant higher angular resolution (FWHM ~3.5arcmin for ALFA versus 
12 arcmin for HIJASS and 15.5 arcmin for HIPASS) will help to limit the confusion 
of sources that plagued those other surveys.
The HIPASS follow-up needed is enormous and therefore has been 
limited to the highest flux sources. It will be years before the sources are 
followed-up (if ever). An ALFA survey will be able to do science with the survey data directly,
without time consuming interferometric follow-up. Additionally, the higher velocity 
resolution of ALFA will be useful in several ways: First, detecting edge-on galaxies 
with peak fluxes near the  noise limit. The edge of a double peak spectrum is much 
sharper at higher velocity resolution which should make it easier to automatically detect these 
sources. Second, the higher velocity resolution will allow more accurate velocity and
velocity width measurements, without the need for follow-up. Even the narrowest sources
will be detected over several channels. Third, since most rfi is
narrow band, the higher frequency resolution will be extremely useful in identifying
and excising rfi. 
</FONT>
<P>
<FONT SIZE="-1">The HIJASS survey has a further serious limitation. Very bad rfi in the
frequency band corresponding to <i>cz</i> ~ 4500 - 7500 km/s (within 
the range of much of the interesting large scale structure e.g., Pisces-Perseus,
A1367-Coma-Great Wall). In addition, HIJASS is not scheduled to do any more observing 
in the Arecibo range (a 4&deg; &times; 4&deg; region in 
Virgo and a few other areas have been covered 
at this point) for the next few years.
</FONT>
<P>
<FONT SIZE="-1">The principal advantage that an Arecibo survey will have over previous surveys
is depth and the number of independent
volumes surveyed. Table B.1 below includes a comparison of the major surveys, including
those discussed here. For comparative purposes, the rms noise per beam quoted for each survey
has been scaled to a velocity resolution of 18 km/s, the resolution of HIPASS.
</FONT>
