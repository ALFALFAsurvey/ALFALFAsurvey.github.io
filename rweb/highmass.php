We have begun a coordinated multiwavelength campaign of these gas-rich 
and very high HI mass galaxies, the <font color="#008000"><b>HIghMass</font></b> survey, which aims
to acquire observational evidence that would confirm or refute the alternative
hypotheses for their existence. The HIghMass targets contain an exceptional, and 
in some cases, dominant fraction of their baryons in the form of cool gas; 
at the same time, most do not meet the extreme optical 
low surface brightness of objects like Malin 1.  </p>

