A widely accepted scenario for how galaxies form and evolve across cosmic time postulates
that galaxies assemble their mass through mergers and the accretion of gas from the
intergalactic medium. Over time, galaxies consume their gas supply as they form stars,
and unless it is replenished, they presumably evolve from 
the blue cloud to the red sequence in the color-magnitude diagram.
In the "down-sizing" evolutionary scenario, the most massive galaxies are most efficient in
consuming their gas reservoirs and, today, are red and gas-poor. Indeed,
the red sequence galaxies are mainly ellipticals and are "red and dead", with
little or no on-going star formation and no detectable atomic or molecular gas 
(the stuff out of which stars are made).</p>

Under such a hypothesis then, very massive galaxies with a substantial supply of cold gas 
are expected to be very rare. One of the most surprising results of the ALFALFA survey
has been the discovery <b>by ALFALFA</b>, because of its combination of high sensitivity and
wide area coverage, of an exceptional population of galaxies with very massive HI disks
and very high fractions of their baryons in the form of gas. The latter is indicated by
the "gas fraction", the ratio of the mass in the form of atomic hydrogen (which ALFALFA
detects) to the mass in the form of stars (as detected by optical/UV/IR telescopes).  
How have these massive dark matter halos managed to acquire and retain such large
amounts of HI gas (more than 10 billion solar masses) and yet avoided turning that
gas into stars? Has star formation
in these objects been somehow inhibited? Are they recent recipients of large
amounts of accreted material from the intergalactic medium? -- in which case, is their cold 
gas largely atomic rather than molecular? Are their HI disks unusually extended?  What role 
does the concentration of their dark matter halo, as characterized by the so-called
"spin parameter", play in the regulation of both gas accretion
and star formation activity?  These issues are of particular 
significance because these high HI mass objects are representative of the 
populations which are likely to dominate future studies of the evolution
of HI in galaxies across cosmic time with the SKA. </p>
